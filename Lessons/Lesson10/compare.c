#include "compare.h"

int isBigger(int x, int y) {
    return x > y;
}
int isSmaller(int x, int y) {
    return x < y;
}
int isEqual(int x, int y) {
    return x == y;
}
int isBiggerEqual(int x, int y) {
    return x >= y;
}
int isSmallerEqual(int x, int y) {
    return x <= y;
}
int isNotEqual(int x, int y) {
    return x != y;
}
